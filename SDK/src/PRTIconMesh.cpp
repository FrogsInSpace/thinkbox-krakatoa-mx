void build_icon_mesh( Mesh& outMesh, float verts[], int faces[], int vertCount, int faceCount ) {
    int baseVertCount = sizeof( baseVerts ) / sizeof( float ) / 3;
    int baseFaceCount = sizeof( baseFaces ) / sizeof( int ) / 5;

    outMesh.FreeAll();
    outMesh.setNumVerts( vertCount + baseVertCount );
    outMesh.setNumFaces( faceCount + baseFaceCount );

    float* baseVertPtr = baseVerts;
    for( int i = 0; i < baseVertCount; ++i, baseVertPtr += 3 )
        outMesh.setVert( i, baseVertPtr );

    for( int i = 0; i < vertCount; ++i, verts += 3 )
        outMesh.setVert( baseVertCount + i, verts );

    int* baseFacesPtr = baseFaces;
    for( int i = 0; i < baseFaceCount; ++i, baseFacesPtr += 5 )
        memcpy( &outMesh.faces[i], baseFacesPtr, sizeof( Face ) );

    for( int i = 0; i < faceCount; ++i, faces += 5 ) {
        faces[0] += baseVertCount;
        faces[1] += baseVertCount;
        faces[2] += baseVertCount;
        memcpy( &outMesh.faces[baseFaceCount + i], faces, sizeof( Face ) );
    }

    outMesh.buildBoundingBox();
}

